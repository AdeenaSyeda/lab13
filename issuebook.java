    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String id = txtid.getText();
        
        BookItem  book = (BookItem) txtbook.getSelectedItem();

         SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-MM-dd");
          String issuedate = Date_Format.format(txttdate.getDate());
          
          SimpleDateFormat Date_Format1 = new SimpleDateFormat("yyyy-MM-dd");
          String returndate = Date_Format1.format(txtrdate.getDate());
       

        try {
            pst = con.prepareStatement("insert into issuebook(name,bookname,issuedate,returndate)values(?,?,?,?)");
            pst.setString(1, id);
            pst.setInt(2, book.id);
            pst.setString(3, issuedate);
            pst.setString(4,returndate);
          

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Book Issued");
            tableUpdate();
            txtid.setText("");
            txtbook.setSelectedIndex(-1);
            txtname.setText("");
            txtname.requestFocus();
            tableUpdate();
            
        } catch (SQLException ex) {
            Logger.getLogger(issuebook.class.getName()).log(Level.SEVERE, null, ex);
        }
   
    }//GEN-LAST:event_jButton1ActionPerformed

